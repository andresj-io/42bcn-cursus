#include "libft.h"

size_t	ft_strlcat(char *restrict dst, const char *restrict src, size_t dstsize)
{
	const char	*s;
	char				*d;
	size_t			n;
	size_t			dlen;
	size_t			slen;

	n = dstsize;
	s = src;
	d = dst;
	slen = ft_strlen(src);
	while (n-- && *d)
		d++;
	dlen = d - dst;
	n = dstsize - dlen;
	if (n == 0)
		return (dlen + slen);
	while (*s)
	{
		if (n != 1)
		{
			*d++ = *s;
			n--;
		}
		s++;
	}
	*d = '\0';
	return (dlen + (s - src));
}
